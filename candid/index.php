                        <h2>Project Overview</h2>

                        <p>In February 2019, Foundation Center and GuideStar merged to become a new organization, Candid. With this merger came entirely new branding for the organization. With over 40 web properties between the two organizations, there was a mountain of rebranding work to be done. This presented an opportunity to streamline our design process among our internal 5-person design team. I was the project lead on developing our first Candid design system on Adobe XD. Based off of components and patterns from Google's Material-UI, we created a shareable design library used both by our internal design team and external consultants.</p>

                        <h2>What I Learned</h2>
                        <p>This project presented huge learning opportunities for me. It was my first time creating and adapting components, a complete gamechanger for my user interface design processes. The design team was constantly in communication about our ongoing projects, and new challenges and branding guidelines were being established as we went. This UI library was being updated in real time as our branding evolved, which was a new challenge that enforced the importance of constant communication with the team through various iterations.</p>
